//Default constructor
String::String(void)
{
  string = NULL;
  size = 0;	
  capacity= 0;	
}


//Constructor from a c-string
String::String(const char * s)
{
  string = new char [capacity];
  memcpy(string,s,capacity*sizeof(*string));
  size = 0;	
  capacity= 0;
}


// ===========================================================================
//                                  Destructor
// ===========================================================================
String::~String(void)
{
  delete string;
  string = NULL;
}

